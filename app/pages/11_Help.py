# TAB 7 — Атрибуция факторов
# ══════════════════════════════════════════════════════════════════════════════
with tabs[6]:
    section_header("Факторная атрибуция: теория и методы")

    st.markdown("""
Факторная атрибуция отвечает на вопрос: **«Какой вклад каждого фактора
в общее изменение целевого показателя?»**
""")

    # ── Аддитивная ──
    with st.expander("Аддитивная атрибуция", expanded=False):
        st.markdown("""
**Предположение:** общее изменение = сумма вкладов отдельных факторов.

**Формула:**
""")
        st.latex(r"\Delta Y = \sum_{i=1}^{k} \Delta x_i \cdot w_i")

        st.markdown("""
где:
- Δxᵢ — абсолютное изменение фактора *i* за период
- wᵢ — вес (коэффициент влияния) фактора *i*

**Когда применять:**
- Факторы влияют на цель независимо и линейно.
- Нет мультипликативных эффектов (взаимодействий).
""")

    # ── Мультипликативная ──
    with st.expander("Мультипликативная атрибуция (логарифмическая)", expanded=False):
        st.markdown("""
**Предположение:** целевой показатель = произведение факторов.
Используется логарифмическое разложение для перехода к аддитивной форме.

**Принцип:**
""")
        st.latex(r"\ln\!\left(\frac{Y_1}{Y_0}\right) = \sum_{i=1}^{k} \ln\!\left(\frac{x_{i,1}}{x_{i,0}}\right)")

        st.markdown("""
Каждое слагаемое — вклад фактора *i* в логарифмическую шкалу.
Для интерпретации в процентных пунктах используется пропорциональное
распределение общего изменения.

**Когда применять:**
- Показатель = произведение компонент (напр., Revenue = Price × Volume).
- Мультипликативная структура KPI.
""")

    # ── Регрессионная ──
    with st.expander("Регрессионная атрибуция (OLS / Ridge)", expanded=False):
        st.markdown("""
Строится линейная регрессия целевой переменной на факторы.
Коэффициенты регрессии используются как веса вклада.

**Модель:**
""")
        st.latex(r"Y = \beta_0 + \sum_{i=1}^{k} \beta_i \cdot X_i + \varepsilon")

        st.markdown("**Вклад фактора *i*:**")
        st.latex(r"\text{contribution}_i = \beta_i \cdot \Delta X_i")

        st.markdown("""
**OLS (обычный МНК):**
""")
        st.latex(r"\hat{\boldsymbol{\beta}} = (\mathbf{X}^\top \mathbf{X})^{-1} \mathbf{X}^\top \mathbf{y}")

        st.markdown("""
**Ridge (с регуляризацией):**
""")
        st.latex(r"\hat{\boldsymbol{\beta}} = (\mathbf{X}^\top \mathbf{X} + \lambda \mathbf{I})^{-1} \mathbf{X}^\top \mathbf{y}")

        st.markdown("""
Ridge предпочтителен при:
- Мультиколлинеарности (корреляции между факторами).
- Малом числе наблюдений.
- Необходимости устойчивых оценок.
""")

    # ── Шепли ──
    with st.expander("Значения Шепли (Shapley values)", expanded=False):
        st.markdown("""
**Теория кооперативных игр:** каждый фактор — «игрок», вклад определяется
как средний маржинальный эффект добавления игрока ко всем возможным коалициям.

**Формула:**
""")
        st.latex(
            r"\phi_i = \sum_{S \subseteq N \setminus \{i\}} "
            r"\frac{|S|!\;(|N|-|S|-1)!}{|N|!} "
            r"\left[ v(S \cup \{i\}) - v(S) \right]"
        )

        st.markdown("""
где:
- *N* — множество всех факторов
- *S* — подмножество (коалиция) без фактора *i*
- *v(S)* — «выигрыш» коалиции *S* (например, R² модели на факторах из *S*)

**Свойства (аксиомы Шепли):**
1. **Эффективность:** сумма всех φᵢ = общий эффект.
2. **Симметрия:** одинаковые факторы получают одинаковый вклад.
3. **Аддитивность:** вклады складываются.
4. **Нулевой игрок:** фактор без влияния получает φ = 0.

**Ограничения:**
- Точный расчёт: O(2ⁿ) — экспоненциальная сложность.
- Для > 10 факторов используются аппроксимации.
- В KIBAD реализован точный расчёт для небольших множеств.
""")

