# TAB 2 — Статистические тесты
# ══════════════════════════════════════════════════════════════════════════════
with tabs[1]:
    section_header("Обзор статистических тестов")

    st.markdown("""
Все тесты в KIBAD возвращают: **статистику теста**, **p-значение**, **размер эффекта**
с текстовой интерпретацией и **бизнес-выводом** на понятном языке.

**Уровень значимости** (α) задаётся в боковой панели (по умолчанию 0.05).
Если p < α — нулевая гипотеза отвергается, различие считается статистически значимым.
""")

    # ── t-тест Уэлча ──
    with st.expander("t-тест Уэлча (Welch's t-test)", expanded=False):
        st.markdown("""
**Когда использовать:**
- Сравнение средних значений **двух независимых групп**.
- Данные — числовые, приблизительно нормально распределённые (при n > 30 — допустимо по ЦПТ).
- Дисперсии групп могут различаться (в отличие от классического t-теста Стьюдента).

**Нулевая гипотеза:** средние двух групп равны (μ₁ = μ₂).

**Формула статистики:**
""")
        st.latex(r"t = \frac{\bar{x}_1 - \bar{x}_2}{\sqrt{\dfrac{s_1^2}{n_1} + \dfrac{s_2^2}{n_2}}}")

        st.markdown("**Степени свободы** рассчитываются по формуле Уэлча–Саттертуэйта:")
        st.latex(
            r"\nu = \frac{\left(\dfrac{s_1^2}{n_1} + \dfrac{s_2^2}{n_2}\right)^2}"
            r"{\dfrac{\left(\dfrac{s_1^2}{n_1}\right)^2}{n_1 - 1} + "
            r"\dfrac{\left(\dfrac{s_2^2}{n_2}\right)^2}{n_2 - 1}}"
        )

        st.markdown("""
**Интерпретация:**
- p < α → средние статистически различаются.
- Смотрите на **доверительный интервал** разности средних для практической значимости.
- Всегда оценивайте **размер эффекта** (Cohen's d), а не только p-значение.
""")

    # ── Манн–Уитни ──
    with st.expander("Критерий Манна–Уитни (Mann–Whitney U)", expanded=False):
        st.markdown("""
**Когда использовать:**
- Сравнение **двух независимых групп**, когда данные **не нормально распределены**.
- Подходит для порядковых данных и выборок с выбросами.
- Непараметрический аналог t-теста.

**Нулевая гипотеза:** распределения двух групп одинаковы.

**Принцип:** все наблюдения ранжируются совместно. Статистика U — это число случаев,
когда значение из группы 1 больше значения из группы 2:
""")
        st.latex(r"U_1 = \sum_{i=1}^{n_1} \sum_{j=1}^{n_2} S(x_{1,i},\; x_{2,j})")
        st.latex(
            r"S(a, b) = \begin{cases} 1 & \text{если } a > b \\ "
            r"0.5 & \text{если } a = b \\ 0 & \text{если } a < b \end{cases}"
        )

        st.markdown("""
**Интерпретация:**
- p < α → распределения различаются (одна группа систематически «выше» другой).
- Для размера эффекта используется **Cliff's delta** (ранговый аналог Cohen's d).
""")

    # ── Хи-квадрат ──
    with st.expander("Критерий хи-квадрат (χ²)", expanded=False):
        st.markdown("""
**Когда использовать:**
- Проверка связи между **двумя категориальными** переменными.
- Таблица сопряжённости (contingency table) с ожидаемыми частотами ≥ 5.

**Нулевая гипотеза:** переменные независимы.

**Формула:**
""")
        st.latex(r"\chi^2 = \sum_{i} \sum_{j} \frac{(O_{ij} - E_{ij})^2}{E_{ij}}")

        st.markdown("где ожидаемые частоты:")
        st.latex(r"E_{ij} = \frac{R_i \cdot C_j}{N}")

        st.markdown("""
- *O* — наблюдаемые частоты, *E* — ожидаемые, *R* — сумма строки, *C* — сумма столбца, *N* — общее число.

**Интерпретация:**
- p < α → переменные связаны.
- Размер эффекта: **Cramér's V** (см. ниже).
- Степени свободы: df = (r − 1)(c − 1).
""")

    # ── Корреляция ──
    with st.expander("Корреляция (Pearson / Spearman)", expanded=False):
        st.markdown("""
**Пирсон** — измеряет **линейную** связь:
""")
        st.latex(
            r"r = \frac{\sum (x_i - \bar{x})(y_i - \bar{y})}"
            r"{\sqrt{\sum (x_i - \bar{x})^2 \cdot \sum (y_i - \bar{y})^2}}"
        )

        st.markdown("**Спирмен** — измеряет **монотонную** (ранговую) связь:")
        st.latex(r"\rho = 1 - \frac{6 \sum d_i^2}{n(n^2 - 1)}")
        st.markdown("где *d* — разность рангов.")

        st.markdown("""
**Шкала интерпретации коэффициента корреляции:**

| |r| | Сила связи |
|-----|-----------|
| 0.00 – 0.19 | Очень слабая |
| 0.20 – 0.39 | Слабая |
| 0.40 – 0.59 | Умеренная |
| 0.60 – 0.79 | Сильная |
| 0.80 – 1.00 | Очень сильная |

**Когда какой использовать:**
- Пирсон — при нормальности и линейной зависимости.
- Спирмен — при отклонениях от нормальности, порядковых данных, нелинейной монотонной связи.
""")

    # ── Размеры эффекта ──
    with st.expander("Размеры эффекта", expanded=False):
        st.markdown("**Cohen's d** (для t-теста) — стандартизированная разность средних:")
        st.latex(r"d = \frac{\bar{x}_1 - \bar{x}_2}{s_{\text{pooled}}}")
        st.latex(
            r"s_{\text{pooled}} = \sqrt{\frac{(n_1 - 1) s_1^2 + (n_2 - 1) s_2^2}{n_1 + n_2 - 2}}"
        )
        st.markdown("""
| d | Интерпретация |
|---|--------------|
| < 0.2 | Пренебрежимый |
| 0.2 – 0.5 | Малый |
| 0.5 – 0.8 | Средний |
| > 0.8 | Большой |
""")

        st.markdown("---")
        st.markdown("**Cramér's V** (для хи-квадрат) — нормированная мера ассоциации категориальных переменных:")
        st.latex(r"V = \sqrt{\frac{\chi^2}{N \cdot (\min(r, c) - 1)}}")
        st.markdown("Диапазон: 0 (нет связи) до 1 (полная связь).")

        st.markdown("---")
        st.markdown("**Cliff's delta** (для Манна–Уитни) — вероятностный размер эффекта:")
        st.latex(r"\delta = \frac{U_1 - U_2}{n_1 \cdot n_2}")
        st.markdown("""
| |δ| | Интерпретация |
|-----|--------------|
| < 0.147 | Пренебрежимый |
| 0.147 – 0.33 | Малый |
| 0.33 – 0.474 | Средний |
| > 0.474 | Большой |
""")

    # ── Мощность и размер выборки ──
    with st.expander("Анализ мощности и размер выборки", expanded=False):
        st.markdown("""
**Мощность теста** (1 − β) — вероятность обнаружить эффект, если он существует.
Рекомендуемая мощность: ≥ 0.80 (80%).

**Формула минимального размера выборки** (для двухвыборочного t-теста):
""")
        st.latex(r"n = \frac{(z_{\alpha/2} + z_{\beta})^2 \cdot 2\sigma^2}{\delta^2}")

        st.markdown("""
где:
- *z*<sub>α/2</sub> — квантиль нормального распределения (при α = 0.05: z = 1.96)
- *z*<sub>β</sub> — квантиль для мощности (при 1 − β = 0.80: z = 0.84)
- *σ* — стандартное отклонение
- *δ* — минимальный значимый размер эффекта (MDE)

**Практический совет:** рассчитайте нужный размер выборки **до** эксперимента.
Запуск теста на слишком маленькой выборке повышает риск ложноотрицательных результатов.
""", unsafe_allow_html=True)

    # ── Множественные сравнения ──
    with st.expander("Поправки на множественные сравнения", expanded=False):
        st.markdown("""
При проведении нескольких тестов одновременно растёт вероятность ложноположительных результатов
(ошибка I рода). KIBAD поддерживает две коррекции:

**1. Поправка Бонферрони** (Family-Wise Error Rate):
""")
        st.latex(r"\alpha_{\text{adj}} = \frac{\alpha}{k}")
        st.markdown("""
где *k* — число одновременно проводимых тестов. Самый строгий метод — контролирует вероятность
хотя бы одного ложного срабатывания. Может быть слишком консервативен при большом *k*.

**2. Процедура Бенджамини–Хохберга** (False Discovery Rate):
""")
        st.markdown("""
1. Отсортировать p-значения по возрастанию: p₍₁₎ ≤ p₍₂₎ ≤ … ≤ p₍ₖ₎.
2. Для каждого i найти порог:
""")
        st.latex(r"\text{порог}_i = \frac{i}{k} \cdot \alpha")
        st.markdown("""
3. Найти максимальное *i*, при котором p₍ᵢ₎ ≤ порог.
4. Отвергнуть все гипотезы от 1 до этого *i*.

FDR контролирует **долю ложных открытий** среди отвергнутых гипотез.
Менее строг, чем Бонферрони, но более мощный.
""")

# ══════════════════════════════════════════════════════════════════════════════
# TAB 3 — Прогнозирование
# ══════════════════════════════════════════════════════════════════════════════
with tabs[2]:
    section_header("Методы прогнозирования временных рядов")

    st.markdown("""
KIBAD предоставляет три метода прогнозирования разной сложности.
Выбор метода зависит от характера данных и задачи.
""")

    # ── Naive ──
    with st.expander("Сезонный наивный прогноз (Seasonal Naive)", expanded=False):
        st.markdown("""
**Идея:** будущее повторяет прошлое. Прогноз на период *t + h* равен значению
в соответствующий период предыдущего сезона.

**Формула:**
""")
        st.latex(r"\hat{y}_{t+h} = y_{t + h - s}")
        st.markdown("""
где *s* — длина сезона (например, 12 для месячных данных с годовой сезонностью).

**Когда использовать:**
- Устойчивая, стабильная сезонность без сильного тренда.
- Как **бейзлайн** для сравнения с более сложными моделями.
- Если сложная модель не побеждает Naive — данные плохо прогнозируемы.

**Ограничения:**
- Не учитывает тренд.
- Не использует экзогенные переменные.
- Плохо работает при структурных сдвигах.
""")

    # ── ARX ──
    with st.expander("Модель ARX (Ridge-регрессия с лагами)", expanded=False):
        st.markdown("""
**Идея:** авторегрессия с экзогенными переменными — линейная модель, где целевая
переменная зависит от своих прошлых значений (лагов) и внешних факторов.

**Формула:**
""")
        st.latex(
            r"y_t = \beta_0 + \sum_{i=1}^{p} \beta_i \cdot y_{t-i} + "
            r"\sum_{j=1}^{q} \gamma_j \cdot x_{t-j} + \varepsilon_t"
        )

        st.markdown("""
где:
- *y*<sub>t−i</sub> — лаги целевой переменной (порядок *p*)
- *x*<sub>t−j</sub> — экзогенные переменные с лагами
- β, γ — коэффициенты, оцениваемые Ridge-регрессией

**Ridge-регуляризация** добавляет штраф на величину коэффициентов:
""", unsafe_allow_html=True)
        st.latex(
            r"\hat{\boldsymbol{\beta}} = \arg\min_{\boldsymbol{\beta}} "
            r"\left\{ \sum_{t} (y_t - \mathbf{X}_t \boldsymbol{\beta})^2 + "
            r"\lambda \|\boldsymbol{\beta}\|_2^2 \right\}"
        )

        st.markdown("""
**Параметры в KIBAD:**
- **Число лагов (p):** сколько прошлых значений использовать (по умолчанию 3).
- **Экзогенные переменные:** выберите из числовых колонок.
- **Горизонт прогноза:** число периодов вперёд.

**Когда использовать:**
- Интерпретируемая модель с понятными коэффициентами.
- Есть внешние драйверы, влияющие на целевую переменную.
- Любой временной ряд — устойчива к переобучению за счёт Ridge.
""")

    # ── SARIMAX ──
    with st.expander("SARIMAX — (p,d,q)(P,D,Q,s)", expanded=False):
        st.markdown("""
**Seasonal ARIMA with eXogenous variables** — наиболее гибкая модель в KIBAD.

**Общая формула (через оператор сдвига B):**
""")
        st.latex(
            r"\Phi(B^s) \, \phi(B) \, (1-B)^d (1-B^s)^D \, y_t = "
            r"\Theta(B^s) \, \theta(B) \, \varepsilon_t + \mathbf{X}_t \boldsymbol{\gamma}"
        )

        st.markdown("""
**Параметры модели:**

| Параметр | Значение | Что контролирует |
|----------|----------|-----------------|
| **p** | Порядок AR | Число лагов в авторегрессионной части |
| **d** | Порядок дифференцирования | Число раз, которое ряд нужно продифференцировать для стационарности |
| **q** | Порядок MA | Число лагов скользящего среднего ошибок |
| **P** | Сезонный AR | Авторегрессия на сезонных лагах |
| **D** | Сезонное дифференцирование | Удаление сезонности дифференцированием |
| **Q** | Сезонный MA | Скользящее среднее ошибок на сезонных лагах |
| **s** | Период сезонности | 12 (месячные), 4 (квартальные), 52 (недельные) |

**Дифференцирование** делает ряд стационарным:
""")
        st.latex(r"(1 - B)^d \, y_t = y_t - y_{t-1} \quad \text{(при } d=1\text{)}")
        st.latex(r"(1 - B^s)^D \, y_t = y_t - y_{t-s} \quad \text{(при } D=1\text{)}")

        st.markdown("""
**Критерии выбора модели:**
- **AIC** (Akaike) — баланс между качеством подгонки и сложностью. Чем меньше, тем лучше.
- **BIC** (Bayesian) — сильнее штрафует за число параметров. Предпочитает более простые модели.

**Когда использовать:**
- Сложная сезонность, сочетание тренда и циклических паттернов.
- Нужно учесть экзогенные переменные.
- Достаточно длинный ряд (≥ 2–3 сезонных цикла).
""")

    # ── Доверительные интервалы ──
    with st.expander("Доверительные интервалы прогноза", expanded=False):
        st.markdown("**Формула интервала:**")
        st.latex(r"\hat{y}_{t+h} \pm z_{\alpha/2} \cdot \hat{\sigma}_{\text{resid}}")

        st.markdown("""
где:
- *z*<sub>α/2</sub> — квантиль нормального распределения (при 95%: z = 1.96)
- *σ̂*<sub>resid</sub> — стандартное отклонение остатков модели

**Параметр «Уровень доверия»** в KIBAD (по умолчанию 95%) управляет шириной полосы.
Чем выше уровень, тем шире полоса.

| Уровень | z |
|---------|---|
| 90% | 1.645 |
| 95% | 1.960 |
| 99% | 2.576 |

**Интерпретация:** при повторении эксперимента 95% интервалов
покроют истинное значение (в частотном смысле).
""", unsafe_allow_html=True)

    # ── Бэктестинг ──
    with st.expander("Бэктестинг (кросс-валидация временных рядов)", expanded=False):
        st.markdown("""
Для оценки качества модели на исторических данных используется метод
**расширяющегося окна** (expanding window):

1. Берётся начальное обучающее окно (минимум *m* точек).
2. Модель обучается на окне, прогнозирует следующие *h* точек.
3. Окно расширяется, процесс повторяется.

**Метрики оценки:**
""")
        st.latex(r"\text{MAE} = \frac{1}{n} \sum_{i=1}^{n} |y_i - \hat{y}_i|")
        st.latex(r"\text{RMSE} = \sqrt{\frac{1}{n} \sum_{i=1}^{n} (y_i - \hat{y}_i)^2}")
        st.latex(r"\text{MAPE} = \frac{100\%}{n} \sum_{i=1}^{n} \left|\frac{y_i - \hat{y}_i}{y_i}\right|")

        st.markdown("""
| Метрика | Что измеряет | Особенности |
|---------|-------------|-------------|
| **MAE** | Средняя абсолютная ошибка | Устойчива к выбросам |
| **RMSE** | Корень средней квадратичной ошибки | Штрафует большие ошибки сильнее |
| **MAPE** | Средний % ошибки | Интуитивна, но не работает при y ≈ 0 |

**Параметры в KIBAD:**
- **Число фолдов** — сколько раз разбивать данные.
- **Минимальный размер обучения** — начальное окно (в точках).
- **Горизонт фолда** — на сколько шагов вперёд прогнозировать.
""")

    # ── Аномалии ──
    with st.expander("Детекция аномалий (rolling z-score)", expanded=False):
        st.markdown("""
Аномалии обнаруживаются методом **скользящего z-score**:
""")
        st.latex(r"z_t = \frac{y_t - \mu_{\text{rolling}}(t)}{\sigma_{\text{rolling}}(t)}")

        st.markdown("""
где:
- μ<sub>rolling</sub> — скользящее среднее с окном *w*
- σ<sub>rolling</sub> — скользящее стандартное отклонение с окном *w*

**Точка считается аномалией**, если |z_t| > порог (по умолчанию 2.5).

**Параметры:**
- **Размер окна (w):** число точек для скользящей статистики.
- **Порог z:** чем выше, тем строже (меньше аномалий).
""", unsafe_allow_html=True)

